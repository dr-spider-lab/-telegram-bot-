# دالة تختار رد عشوائي
# ═══════════════════════════════════════
def get_reply(message: str) -> str:
    msg = message.lower().strip()
    
    # تحيات
    if any(word in msg for word in ["اهلا", "مرحبا", "هلا", "السلام", "سلام", "هاي", "hi", "hello", "hey"]):
        return random.choice(HI_REPLIES)
    
    # إزيك
    if any(word in msg for word in ["إزيك", "ازيك", "اخبارك", "أخبارك", "عامل ايه", "عامل إيه", "كيفك", "how are you"]):
        return random.choice(HOW_ARE_YOU)
    
    # بتعمل إيه
    if any(word in msg for word in ["بتعمل ايه", "بتعمل إيه", "بتعمل اي", "بتعمل أي", "عامل ايه", "عامل إيه", "what are you doing", "what doing"]):
        return random.choice(WHAT_DOING)
    
    # سعر
    if any(word in msg for word in ["سعر", "بكام", "فلوس", "تكلفة", "price", "cost", "how much"]):
        return random.choice(PRICE_REPLIES)
    
    # معاد
    if any(word in msg for word in ["معاد", "موعد", "متى", "امتى", "إمتى", "وقت", "time", "when", "appointment"]):
        return random.choice(TIME_REPLIES)
    
    # شكر
    if any(word in msg for word in ["شكر", "thanks", "thank you", "thx", "merci"]):
        return random.choice(THANKS_REPLIES)
    
    # باي
    if any(word in msg for word in ["باي", "مع السلامة", "سلام", "bye", "goodbye", "see you"]):
        return random.choice(BYE_REPLIES)
    
    # مين
    if any(word in msg for word in ["مين", "من", "who", "who are you", "what are you"]):
        return random.choice(WHO_REPLIES)
    
    # فين
    if any(word in msg for word in ["فين", "فينك", "وين", "where", "where are you"]):
        return random.choice(WHERE_REPLIES)
    
    # لو مفيش تطابق — رد عشوائي من الـ default
    return random.choice(DEFAULT_REPLIES)

# ═══════════════════════════════════════
async def smart_reply(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user_message = update.message.text
    reply = get_reply(user_message)
    await update.message.reply_text(reply)

# ═══════════════════════════════════════
def main():
    app = Application.builder().token(TELEGRAM_TOKEN).build()
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, smart_reply))
    print("🤖 البوت شغال وبيستنى الرسايل...")
    app.run_polling()

if __name__ == "__main__":
    main()
